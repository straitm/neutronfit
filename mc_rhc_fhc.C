  fhc_b12.Add(fhc_reco_numu,    sigfrac * mum_b12yield);
  fhc_b12.Add(fhc_reco_numubar, sigfrac * mup_b12yield);
  fhc_b12.Divide(fhc);

  TH1D * doublerat_ncomplications = (TH1D*)rhc_neutrons->Clone("doublerat_ncomplications");
